class Stack:
    def __init__(self):
        self.stack_list = []

    def print_stack(self):
        for i in range(len(self.stack_list)-1, -1, -1):
            print(self.stack_list[i])

    def is_empty(self):
        return len(self.stack_list) == 0

    def peek(self):
        if self.is_empty():
            return None
        else:
            return self.stack_list[-1]

    def size(self):
        return len(self.stack_list)

    def push(self, value):
        self.stack_list.append(value)

    def pop(self):
        if self.is_empty():
            return None
        else:
            return self.stack_list.pop()



def is_balanced_parentheses(value):
    # create a new stack
    stack = Stack()
    # Iterate over the each character in the string
    for char in value:
    # check the ch if opening push to stack
        if char == "(":
            stack.push(char)
    # if closing
        elif char == ")":
            # pop from stack and check closing or
            # check the stack is empty
            if stack.is_empty() or stack.pop() == ")":
                # Return False
                return False
    # check the stack if empty return True
    return stack.is_empty()


